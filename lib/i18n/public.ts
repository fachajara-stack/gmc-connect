export type PublicLang = 'fr' | 'es' | 'en'

export const PUBLIC_LANG_COOKIE = 'gmc-public-lang'

export function readPublicLang(): PublicLang {
  const m = document.cookie.match(/(?:^|;\s*)gmc-public-lang=([^;]+)/)
  const stored = m?.[1]
  if (stored === 'fr' || stored === 'es' || stored === 'en') return stored
  // Détection navigateur
  const nav = navigator.language.toLowerCase()
  if (nav.startsWith('es')) return 'es'
  if (nav.startsWith('fr')) return 'fr'
  return 'es'
}

export function writePublicLang(lang: PublicLang) {
  document.cookie = `${PUBLIC_LANG_COOKIE}=${lang};path=/;max-age=31536000;SameSite=Lax`
}

